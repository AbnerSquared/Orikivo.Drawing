namespace Orikivo.Poxel
{
    /// <summary>
    /// A collection of max size ratios for a Discord.Embed.
    /// </summary>
    public enum EmbedMediaRatio
    {
        /// <summary>
        /// The image has a ratio of 16:9.
        /// </summary>
        Widescreen = 1,

        /// <summary>
        /// The image has a ratio of 2:1.
        /// </summary>
        Wide = 2,

        /// <summary>
        /// The image has a ratio of 4:3.
        /// </summary>
        Rectangle = 3,

        /// <summary>
        /// The image has a ratio of 1:1.
        /// </summary>
        Square = 4,

        /// <summary>
        /// The image has a ratio of 1:2.
        /// </summary>
        Tall = 5

    }
}
